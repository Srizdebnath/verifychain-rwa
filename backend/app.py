w3 = Web3(Web3.HTTPProvider(CELO_RPC))
account = w3.eth.account.from_key(PRIVATE_KEY)

def send_transaction(function_call):
    nonce = w3.eth.get_transaction_count(account.address)
    tx = function_call.build_transaction({
        'chainId': 11142220, 
        'gas': 2000000,
        'gasPrice': w3.eth.gas_price,
        'nonce': nonce,
    })
    signed_tx = w3.eth.account.sign_transaction(tx, PRIVATE_KEY)
    tx_hash = w3.eth.send_raw_transaction(signed_tx.rawTransaction)
    return w3.to_hex(tx_hash)

def get_real_bond_yield():
    try:
       
        ticker = yf.Ticker("^TNX") 
        data = ticker.history(period="1d")
        if data.empty:
            return 7.20
        latest_yield = data['Close'].iloc[-1]
        return float(latest_yield)
    except Exception as e:
        print(f"Error fetching yield: {e}")
        return 7.20 
@app.route('/analyze_and_oracle', methods=['POST'])
def analyze_and_oracle():
    if 'file' not in request.files: return jsonify({"error": "No file"}), 400
    file = request.files['file']
   
    reader = PdfReader(file)
    text = ""
    for page in reader.pages: text += page.extract_text()

    client = Groq(api_key=os.getenv("GROQ_API_KEY"))
    prompt = f"Extract JSON: bond_name, isin, face_value_amount (number only). Text: {text[:4000]}"
    
    completion = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[{"role": "user", "content": prompt}]
    )
    
    try:
        content = completion.choices[0].message.content
        start = content.find('{')
        end = content.rfind('}') + 1
        if start != -1 and end != -1:
            json_str = content[start:end]
            ai_data = json.loads(json_str)
        else:
            raise ValueError("No JSON found")
    except Exception as e:
        print(f"AI Parse Error: {e}")
        ai_data = {"bond_name": "Unknown", "isin": "UNKNOWN", "face_value_amount": 0}


    real_yield = get_real_bond_yield()
    

    return jsonify({
        "ai_analysis": ai_data,
        "oracle_data": {
            "source": "Yahoo Finance API",
            "ticker": "^TNX (10Y Yield)",
            "live_yield": real_yield,
            "timestamp": time.time()
        }
    })

